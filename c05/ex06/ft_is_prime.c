#include <stdio.h>
int	ft_is_prime(int	nb)
{
	int	i;

	i = 2;
	if (nb <= 1)
		return (0);
	if (nb == 2147483647)
		return (1);
	while (nb > i)
	{
		if ((nb % i) == 0)
			return (0);
		i++;
	}
	return (1);
}

int	main()
{
	printf("%d", ft_is_prime(2147483645));
}
