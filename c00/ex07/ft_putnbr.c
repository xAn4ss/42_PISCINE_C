#include <unistd.h>

void	ft_putchar(char	c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int	nb)
{
	int	x;

	x = 1;
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (nb < 0)
		{
			ft_putchar('-');
			nb = nb * -1;
		}
		while (x <= (nb / 10))
		{
			x *= 10;
		}
		while (x > 0)
		{
			ft_putchar((nb / x) + '0');
			nb = nb % x;
			x /= 10;
		}
	}
}
